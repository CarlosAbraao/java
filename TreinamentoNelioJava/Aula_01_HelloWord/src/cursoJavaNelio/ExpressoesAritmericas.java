package cursoJavaNelio;

public class ExpressoesAritmericas {

	public static void main(String[] args) {
		
	// OPERADORES "+" "-" "*" "/" "%"
		
		// EXEMPLOS COM USO DE VARIAVEIS
		
		double valor1=24;
		double valor2=65;
		double valor3=45;
		double valor4=12;
		System.out.println("Operação/Soma");
		System.out.println(valor1 + valor2);
		System.out.println("Operação/Subtração");
		System.out.println(valor4 - valor2);
		System.out.println("Operação/Divisão");
		System.out.println(valor3 / valor2);
		System.out.println("Operação/Multiplicação");
		System.out.println(valor2 * valor2);
		System.out.println("Operação/Resto da divisão");
		System.out.println(valor1 % valor4);
		
		
		

	}

}
