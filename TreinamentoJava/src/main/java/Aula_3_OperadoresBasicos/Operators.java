package Aula_3_OperadoresBasicos;

public class Operators {
    public static void main(String[] args) {

        double primeiro = 11;
        double segundo = 2;

        // Operadores básicos

        // soma (+)

        System.out.println(primeiro + segundo);

        // subtração (-)

        System.out.println(primeiro - segundo);

        //multiplicação

        System.out.println(primeiro * segundo);

        //divisão (/)

        System.out.println(primeiro / segundo);

        // módulo ==> o mesmo que o resto da divisão (%)

        System.out.println("O resto da divisão é´: " + (primeiro % segundo));




    }
}
