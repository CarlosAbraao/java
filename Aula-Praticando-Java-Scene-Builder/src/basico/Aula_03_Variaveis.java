package basico;

public class Aula_03_Variaveis {

	public static void main(String[] args) {
		/**
		 * VARIAVEIS SÃO LOCAIS DE ARMAZENAMENTO DE DADOS
		 * VARIAVEIS PRECISAM SER DECLARADAS
		 * EX DE USO: <TIPO> <NOME> = <VALOR DA VARIAVEL>;
		 * 
		 * ALGUNS TIPOS DE VARIAVEIS
		 * 
		 * 
		 * byte
		 * short
		 * int
		 * long
		 * double
		 * float
		 * char
		 * boolean
		 * String (essa variavel representa uma cadeira de caracteres)	
	
		 * **/
		
		//EXEMPLO USO PRÁTICO
		
		int idade = 35;
		double valor = 15.50;
		String nome = "Carlos Abraão";
		
		//IMPRIMINDO A VARIAVEL
		
		System.out.println(nome);
		System.out.println(idade);
		System.out.println(valor);

	}

}
