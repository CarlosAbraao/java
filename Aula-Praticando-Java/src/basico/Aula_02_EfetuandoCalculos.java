package basico;

public class Aula_02_EfetuandoCalculos {

	public static void main(String[] args) {
		/**
		 * 5 PRINCIPAIS OPERADORES ARITMÉTICOS
		 * PARA SOMAR VC VAI USAR O SIMBOLO (SINAL) " + "
		 * PARA SUBTRAIR VC VAI USAR O SIMBOLO (SINAL) " - "
		 * PARA MULTIPLICAR VC VAI USAR O SIMBOLO (SINAL) " * "
		 * PARA DIVIDIR VC VAI USAR O SIMBOLO (SINAL) " / "
		 * PARA SABER O RESTO DA DIVISÃO VC VAI USAR O SIMBOLO (SINAL) " % "
		 * **/

		
		/** VAMOS CALCULAR? **/
		
		System.out.println(2 + 1);
		System.out.println(2 - 1);
		System.out.println(2 * 1);
		System.out.println(2 / 1);
		
		//NESSE CASO; 2 DIVIDIDO POR 1, É 1, 0 RESTO DA DIVISÃO É 0.
		System.out.println(2 % 1);
	}

}
